#include <iostream>

using namespace std;

int main(int argc, char **argv)
{
    int i(2), j(8);

    cout << i << endl;
    cout << i++ << endl;
    cout << i << endl;
    cout << ++i << endl;
    cout << j << endl;
    cout << j-- << endl;
    cout << j << endl;
    cout << --j << endl;
    return 0;
}
